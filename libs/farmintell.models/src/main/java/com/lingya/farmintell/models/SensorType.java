package com.lingya.farmintell.models;

/**
 * 传感器类型
 */
public enum SensorType {
    /**
     * 空气温度
     */
    temp,
    /**
     * 空气湿度
     */
    hum,
    /**
     * 二氧化碳 含量 百分比
     */
    co2,
    /**
     * 光照强度
     */
    light,
    /**
     * 土壤 含水率
     */
    soilWater,
    /**
     * 土壤温度
     */
    soilTemp
}
